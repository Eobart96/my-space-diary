    if command -v hostname &> /dev/null; then
        HOST_IP=$(hostname -I | awk '{print $1}')
    else
        HOST_IP="localhost"
    fi
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ health endpoint
    if curl -f -s "http://$HOST_IP:3000/health" > /dev/null; then
        log_success "–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω–æ –∏ –æ—Ç–≤–µ—á–∞–µ—Ç –Ω–∞ –∑–∞–ø—Ä–æ—Å—ã"
    else
        log_warning "–ü—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –º–æ–∂–µ—Ç –Ω–µ —Ä–∞–±–æ—Ç–∞—Ç—å –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ - –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏"
    fi
else
    log_warning "curl –Ω–µ –Ω–∞–π–¥–µ–Ω, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É —Ä–∞–±–æ—Ç–æ—Å–ø–æ—Å–æ–±–Ω–æ—Å—Ç–∏"
fi
echo ""

# –®–∞–≥ 9: –ü–æ–¥–≥–æ—Ç–æ–≤–∫–∞ –∫ –∫–æ–º–º–∏—Ç—É (–µ—Å–ª–∏ –µ—Å—Ç—å –∏–∑–º–µ–Ω–µ–Ω–∏—è)
log_status "üìù –ü—Ä–æ–≤–µ—Ä–∫–∞ –∏–∑–º–µ–Ω–µ–Ω–∏–π –¥–ª—è –∫–æ–º–º–∏—Ç–∞..."
if [ -n "$(git status --porcelain)" ]; then
    log_status "üîß –û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –∏–∑–º–µ–Ω–µ–Ω–∏—è, –¥–æ–±–∞–≤–ª—è–µ–º —Ñ–∞–π–ª—ã..."
    git add .
    
    log_status "üíæ –°–æ–∑–¥–∞–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞..."
    git commit -m "$COMMIT_MESSAGE"
    
    log_status "üì§ –û—Ç–ø—Ä–∞–≤–∫–∞ –∏–∑–º–µ–Ω–µ–Ω–∏–π –Ω–∞ GitHub..."
    git push origin main
    
    log_success "–ò–∑–º–µ–Ω–µ–Ω–∏—è —É—Å–ø–µ—à–Ω–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã –Ω–∞ GitHub"
else
    log_success "–ù–µ—Ç –∏–∑–º–µ–Ω–µ–Ω–∏–π –¥–ª—è –∫–æ–º–º–∏—Ç–∞ - –ø—Ä–æ–µ–∫—Ç –≤ —á–∏—Å—Ç–æ–º —Å–æ—Å—Ç–æ—è–Ω–∏–∏"
fi
echo ""

# –®–∞–≥ 10: –§–∏–Ω–∞–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è
echo "=================================================="
log_success "‚úÖ –ü—Ä–æ–µ–∫—Ç —É—Å–ø–µ—à–Ω–æ –æ—á–∏—â–µ–Ω, —Å–±—Ä–æ—à–µ–Ω –∏ —Ä–∞–∑–≤–µ—Ä–Ω—É—Ç!"
echo ""
echo "üìã –ò—Ç–æ–≥–æ–≤–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:"
echo "   - –ü—Ä–æ–µ–∫—Ç –ø—Ä–∏–≤–µ–¥–µ–Ω –∫ —Å–æ—Å—Ç–æ—è–Ω–∏—é –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∫–æ–º–º–∏—Ç–∞"
echo "   - –í—Å–µ –ª–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è —É–¥–∞–ª–µ–Ω—ã"
echo "   - –ó–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã –∑–∞–Ω–æ–≤–æ"
echo "   - Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã —Å–æ–±—Ä–∞–Ω—ã –∏ –∑–∞–ø—É—â–µ–Ω—ã"
echo ""

# –ü–æ–∫–∞–∑–∞—Ç—å –ø–æ–ª–µ–∑–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã
echo "üîß –ü–æ–ª–µ–∑–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã:"
echo "   docker-compose logs -f          # –ü—Ä–æ—Å–º–æ—Ç—Ä –ª–æ–≥–æ–≤"
echo "   docker-compose ps               # –°—Ç–∞—Ç—É—Å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤"
echo "   docker-compose restart          # –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ —Å–µ—Ä–≤–∏—Å–æ–≤"
echo "   git status                      # –°—Ç–∞—Ç—É—Å Git"
echo ""

if [ -f "docker-compose.yml" ]; then
    echo "üì± –î–æ—Å—Ç—É–ø–Ω—ã–µ URL:"
    if command -v hostname &> /dev/null; then
        HOST_IP=$(hostname -I | awk '{print $1}')
        echo "   - –§—Ä–æ–Ω—Ç–µ–Ω–¥: http://$HOST_IP:3000"
        echo "   - API: http://$HOST_IP:3000/api"
    else
        echo "   - –§—Ä–æ–Ω—Ç–µ–Ω–¥: http://localhost:3000"
        echo "   - API: http://localhost:3000/api"
    fi
    echo ""
fi

echo "üéâ –ì–æ—Ç–æ–≤–æ! –ü—Ä–æ–µ–∫—Ç –≥–æ—Ç–æ–≤ –¥–ª—è –Ω–æ–≤–æ–π —Ä–∞–±–æ—Ç—ã."
