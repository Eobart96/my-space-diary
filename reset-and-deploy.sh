echo ""

# –®–∞–≥ 2: –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏ —É–¥–∞–ª–µ–Ω–∏–µ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤
log_status "üõë –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏ —É–¥–∞–ª–µ–Ω–∏–µ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤..."
if [ -f "docker-compose.yml" ]; then
    docker-compose down --remove-orphans || true
    docker system prune -f || true
    log_success "Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"
else
    log_warning "docker-compose.yml –Ω–µ –Ω–∞–π–¥–µ–Ω, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º –æ—Å—Ç–∞–Ω–æ–≤–∫—É –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤"
fi
echo ""

# –®–∞–≥ 3: –°–±—Ä–æ—Å Git –∏–∑–º–µ–Ω–µ–Ω–∏–π
log_status "üîÑ –°–±—Ä–æ—Å –≤—Å–µ—Ö –ª–æ–∫–∞–ª—å–Ω—ã—Ö –∏–∑–º–µ–Ω–µ–Ω–∏–π Git..."
