git reset --hard origin/main
log_success "–ü—Ä–æ–µ–∫—Ç –ø—Ä–∏–≤–µ–¥–µ–Ω –∫ —Å–æ—Å—Ç–æ—è–Ω–∏—é –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ –∫–æ–º–º–∏—Ç–∞ –Ω–∞ GitHub"
echo ""

# –®–∞–≥ 5: –û—á–∏—Å—Ç–∫–∞ node_modules –∏ –¥—Ä—É–≥–∏—Ö —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤
